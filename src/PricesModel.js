import { DataTypes } from 'sequelize'
import { sequelize } from './database.js'


// model is a table in database
// primary keys: {chainId, tokenAddr, timestamp}
export const ChainlinkPriceModel = sequelize.define('ChainlinkPrice', {
    chainId: {
        type: DataTypes.INTEGER,
        allowNull: false,
        primaryKey: true
    },
    token: {  // address of token
        type: DataTypes.STRING,
        allowNull: false,
        primaryKey: true
    },
    timestamp: {
        type: DataTypes.INTEGER,
        allowNull: false,
        primaryKey: true
    },
    value: {
        type: DataTypes.DOUBLE,
        allowNull: false
    }
}, {
    timestamps: false  // disable autogenerated timestamp by sequelize
});
